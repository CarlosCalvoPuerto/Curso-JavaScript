

/** OPERADORES ASIGNACION [INCREMENTO Y DECREMENTO ] */

let a = 1;
document.write('Valor de a: ', a);
document.write('<br>');

a++; // (a+1)
document.write('Valor de a: ', a);
document.write('<br>');

a+=5; // (a+b)
document.write('Valor de a: ', a);
document.write('<br>');

a--; // (a-1)
document.write('Valor de a: ', a);
document.write('<br>');

a-=3; // (a-b)
document.write('Valor de a: ', a);
document.write('<br>');

a*=2; // (a*b)
document.write('Valor de a: ', a);
document.write('<br>');

a/=3; // (a/b)
document.write('Valor de a: ', a);
document.write('<br>');

a**=3; // (a^b)
document.write('Valor de a: ', a);
document.write('<br>');

