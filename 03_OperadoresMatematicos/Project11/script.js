

/** OPERADORES DE COMPARACION */

let val1, val2;

val1 = 20;
val2 = 10;

document.write(val1, ' > ',val2, ': ', val1 > val2);    // Mayor
document.write('<br>')
document.write(val1, ' < ',val2, ': ', val1 < val2);    // Menor
document.write('<br>')
document.write(val1, ' = ',val2, ': ', val1 == val2);   // Igual
document.write('<br>')
document.write(val1, ' != ',val2, ': ', val1 != val2);  // Distinto
document.write('<br>')
document.write(val1, ' >= ',val2, ': ', val1 >= val2);    // Mayor/Igual
document.write('<br>')
document.write(val1, ' <= ',val2, ': ', val1 <= val2);    // Menor/Igual






