

/** OPERADORES MATEMATICOS */

/** Declarar Variables de Funcion*/
let a, b, sum, res, mult, div, resid, poten;

// Pedir datos al usuario
a = parseInt(prompt('INGRESE EL VALOR 1...'));
b = parseInt(prompt('INGRESE EL VALOR 2...'));

// Suma
sum = a + b;
// Resta
res = a - b;
// Multiplicacion
mult = a * b;
// Division
div = a / b;
// Resto/Residuo
resid = a % b;
// Potencia
poten = a ** b;

// Mostrar resultado
document.write('Suma: ' + sum);
document.write('<br>');
document.write('Resta: ' + res);
document.write('<br>');
document.write('Multiplicacion: ' + mult);
document.write('<br>');
document.write('Division: ' + div);
document.write('<br>');
document.write('Resto: ' + resid);
document.write('<br>');
document.write('Potencia: ' + poten);




