

/** FUNCIONES MATEMATICAS */

// Redondear
var precio = Math.round(399.45);        
document.write('Precio redondeado: ', precio);
document.write('<br>');

// Redondear hacia arriba siempre
var precio = Math.ceil(299.2);          
document.write('Precio redondeado: ', precio);
document.write('<br>');

// Redondear hacia abajo siempre
var precio = Math.floor(540.9);         
document.write('Precio redondeado: ', precio);
document.write('<br>');

// Seno de un angulo en radianes (45)
var seno = Math.sin(Math.PI/4);         
document.write('Seno de 45: ', seno);
document.write('<br>');

// Exponencial de un numero en base "e"
var expo = Math.exp(2);                 
document.write('Exponencial de 2: ', expo);
document.write('<br>');

// Logaritmo neperiano de un numero
var logar = Math.log(10);               
document.write('Logaritmo de 10: ', logar);
document.write('<br>');

// Absoluto de un numero
var absol = Math.abs(-10);              
document.write('Absoluto de -10: ', absol);
document.write('<br>');

// Numero aleatorio
var rand = Math.ceil(Math.random()*20);            
document.write('Numero aleatorio: ', rand);
document.write('<br>');

// Raiz cuadrada
var raiz = Math.sqrt(81);               
document.write('Raiz cuadrada de 81: ', raiz);
document.write('<br>');

// Exponente de "a" sobre "b"
var exponen = Math.pow(4,2);            
document.write('4 ^ 2: ', exponen);
document.write('<br>');



